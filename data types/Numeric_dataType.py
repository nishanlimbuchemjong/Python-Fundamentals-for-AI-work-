"""
Numberic Data Type in Python:
    Numeric data type in Python represents the data that has a numeric value.
    A numeric value can be an integer, a floating number, or even a complex number.
    i. Integer: his value is represented by int class. 
        It contains positive or negative whole numbers (without fractions or decimals).
    ii. Float: his value is represented by the float class. 
        It is a real number with a floating-point representation. 
        It is specified by a decimal point. 
    iii. Complex Number: A complex number is represented by a complex class. 
        It is specified as (real part) + (imaginary part)j .
"""
# For Example:

x = 5
print(type(x))      # output: <class 'int'>

y = 5.0
print(type(y))      # output: <class 'float'>

z = 2 + 5j
print(type(z))      # output: <class 'complex'>